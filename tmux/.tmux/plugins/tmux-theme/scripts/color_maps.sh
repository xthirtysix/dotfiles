set_theme() {
    case $1 in
        rosepine)
            white=$rosepine_text
            gray=$rosepine_surface
            dark_gray=$rosepine_base
            light_purple=$rosepine_rose
            dark_purple=$rosepine_base
            cyan=$rosepine_foam
            green=$rosepine_love
            light_green=$rosepine_pine
            orange=$rosepine_gold
            red=$rosepine_love
            pink=$rosepine_rose
            yellow=$rosepine_gold
            blue=$rosepine_blue
            ;;
        nordfox)
            white=$nordfox_text
            gray=$nordfox_bg1
            dark_gray=$nordfox_bg0
            light_purple=$nordfox_magenta
            dark_purple=$nordfox_bg0
            cyan=$nordfox_green
            green=$nordfox_red
            light_green=$nordfox_green
            orange=$nordfox_orange
            red=$nordfox_red
            pink=$nordfox_pink
            yellow=$nordfox_yellow
            blue=$nordfox_blue
            ;;
    esac
}
